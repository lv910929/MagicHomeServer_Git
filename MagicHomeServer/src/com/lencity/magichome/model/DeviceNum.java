package com.lencity.magichome.model;

public class DeviceNum {

	private Integer weekdayId;
	
	private Integer onlineNum;
	
	private Integer offlineNum;

	public DeviceNum() {
		super();
	}

	public Integer getWeekdayId() {
		return weekdayId;
	}

	public void setWeekdayId(Integer weekdayId) {
		this.weekdayId = weekdayId;
	}

	public Integer getOnlineNum() {
		return onlineNum;
	}

	public void setOnlineNum(Integer onlineNum) {
		this.onlineNum = onlineNum;
	}

	public Integer getOfflineNum() {
		return offlineNum;
	}

	public void setOfflineNum(Integer offlineNum) {
		this.offlineNum = offlineNum;
	}
	
	
}
